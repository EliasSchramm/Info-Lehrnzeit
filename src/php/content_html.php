<h2>Kurze Einleitung</h2>
<p>HTML ist die Sprache welche <u>beschreibt</u>, welche Elemente mit welchem Inhalt sich auf der Website befinden.</p>
<p>Mit dieser Sprache ist es möglich die Website zu strukturieren (Text, Links, Formulare, Bilder, ... einfügen), sowie Text grob zu formatieren.</p>
<p>Style erhält die Website mit CSS und Funktion erhält die Website durch JavaScript.</p>
